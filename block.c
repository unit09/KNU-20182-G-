void paintBlock(cur current, int num, int shape){
	int i, j;
	
	for(i = 0; i < 4; i++){
		for(j = 0; j < 4; j++){
			move(current.y+i, current.x+j);
			if(block[num][shape][i][j] == 1){
				addch('O');
			}				
		}
	}
}
